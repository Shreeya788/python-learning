'''
Typecasting -> changing one datatype to another datatype

Two typecasting methods :

- Implicit 
    => Performed automatically by python's compiler to prevent data loss
    => example : 145.5 converted to 146
- Explicit 
    => Explicitly converting datatype using typecasting function such as str(), int(),
    float(), ord(), hex(), tuple(), list(), dict()
'''


x= str('11')
print(type(x))
y= float('10')
print(type(y))
print(y)